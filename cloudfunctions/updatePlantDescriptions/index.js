exports.main = async (event, context) => {
  try {
    // 获取所有植物记录
    const allPlants = await db.collection('plants').limit(500).get()
    const records = allPlants.data || []
    
    let updated = 0
    let notFound = 0
    
    for (const record of records) {
      const name = record.name
      const desc = descriptions[name]
      if (desc) {
        await db.collection('plants').doc(record._id).update({
          data: {
            简介: desc,
            updatedAt: db.serverDate()
          }
        })
        updated++
      } else {
        notFound++
      }
    }
    
    return {
      success: true,
      total: records.length,
      updated,
      notFound
    }
  } catch (e) {
    return {
      success: false,
      error: e.message
    }
  }
}
